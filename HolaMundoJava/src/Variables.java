public class Variables {
    public static void main(String[] args) {
        int speed;
        speed = 10;
        System.out.println("speed = " + speed);
        int salary = 100;
        String employeeName = "Aldo";
        System.out.println("employeeName = " + employeeName);
        System.out.println("salary = " + salary);
    }
}
